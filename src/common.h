#ifndef _COMMON_
#define _COMMON_

#include <assert.h>
#include <stdbool.h>
#include <stdint.h>

///////////////////////////////////////////////////////////////////////////////
// Types
///////////////////////////////////////////////////////////////////////////////
typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef float f32;
typedef double f64;

///////////////////////////////////////////////////////////////////////////////
// Generic macros
///////////////////////////////////////////////////////////////////////////////
#define MIN(a, b) (((a) < (b)) ? (a) : (b))
#define MAX(a, b) (((a) > (b)) ? (a) : (b))

///////////////////////////////////////////////////////////////////////////////
// In Between
///////////////////////////////////////////////////////////////////////////////
static inline bool in_between_i32(i32 min, i32 b, i32 max)
{
    assert(min < max);
    return min <= b && b <= max;
}

#endif
